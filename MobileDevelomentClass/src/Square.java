public class Square {

	public static void main(String[] args) {
		System.out.println("*********Square of first ten numbers*********");
		for (int m = 1; m <= 10; m++) {
			if(m!=10)
				System.out.println(m + "  -->  " + m * m);
			else
				System.out.println(m + " -->  " + m * m);
		}
	}

}
